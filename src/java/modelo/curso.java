package modelo;

/**
 *
 * @author carlosmc
 */
public class curso {
    
    
    String codigo;
    String nombre;
    String tiempo;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTiempo() {
        return tiempo;
    }

    public void setTiempo(String tiempo) {
        this.tiempo = tiempo;
    }

    public curso(String codigo, String nombre, String tiempo) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.tiempo = tiempo;
    }
    
    
}
